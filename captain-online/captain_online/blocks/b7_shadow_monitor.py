def _infer_entry(signal: dict) -> float | None:
    """Infer entry price from signal features or per_account data."""
    # Direct entry price from OR tracker
    features = signal.get("features", {})
    if isinstance(features, dict) and features.get("entry_price"):
        return float(features["entry_price"])

    # From per_account — take first account's entry if available
    per_account = signal.get("per_account", {})
    for ac_id, ac_detail in per_account.items():
        if isinstance(ac_detail, dict) and ac_detail.get("entry_price"):
            return float(ac_detail["entry_price"])

    # Compute from TP/SL midpoint as last resort (ORB strategy: entry is between TP and SL)
    tp = signal.get("tp_level")
    sl = signal.get("sl_level")
    direction = signal.get("direction", 0)
    if tp is not None and sl is not None and direction != 0:
        # For ORB: entry divides TP-SL range at sl_multiple/(tp_multiple+sl_multiple)
        # Default tp_multiple=0.70, sl_multiple=0.35 → entry is 1/3 from SL toward TP
        # Simpler: entry = SL + (TP-SL) * sl_frac where sl_frac = 0.35/(0.35+0.70) = 1/3
        if direction == 1:
            return sl + (tp - sl) / 3.0
        else:
            return tp + (sl - tp) / 3.0

    return None


def _get_shadow_contracts(signal: dict) -> int:
    """Get contract count from signal's per_account breakdown."""
    per_account = signal.get("per_account", {})
    for ac_id, ac_detail in per_account.items():
        if isinstance(ac_detail, dict):
            contracts = ac_detail.get("contracts", 0)
            if contracts > 0:
                return contracts
    return 1  # Default to 1 for per-contract normalization


def _get_point_value(asset_id: str) -> float:
    """Get point value for asset from D00 (used for P&L calculation)."""
    try:
        with get_cursor() as cur:
            cur.execute(
                "SELECT point_value FROM asset_universe "
                "LATEST ON ts PARTITION BY asset_id "
                "WHERE asset_id = $1",
                [asset_id],
            )
            row = cur.fetchone()
            if row and row[0] is not None:
                return float(row[0])
    except Exception:
        logger.warning("_get_point_value: D00 lookup failed for %s, using default 50.0", asset_id)
    return 50.0


def _get_live_price(asset_id: str) -> float | None:
    """Get live price from TopstepX stream cache."""
    contract_id = resolve_contract_id(asset_id)
    if not contract_id:
        return None
    quote = quote_cache.get(contract_id)
    if quote and quote.get("lastPrice"):
        return float(quote["lastPrice"])
    return None
